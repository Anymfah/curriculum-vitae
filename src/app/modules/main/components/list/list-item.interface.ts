
export interface ListItem {

  /**
   * ID of the list item.
   */
  id: number;

  /**
   * The title of the list item.
   */
  title: string;

  /**
   * The subtitle of the list item.
   */
  subtitle?: string;

  /**
   * Icon of the list item.
   */
  icon?: string;

  /**
   * The color of the list item.
   */
  color?: string;

}
